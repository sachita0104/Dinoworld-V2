export const getAquaticDinosaurs  = () => DINOSAURS.filter(d => d.isAquatic);
export const getDinosaurBySlug    = (slug: string) => DINOSAURS.find(d => d.slug === slug);

export const filterDinosaurs = (
  query: string, diet: string, period: string, habitat: string
) => DINOSAURS.filter(d => {
  const q = query.toLowerCase();
  return (
    (d.name.toLowerCase().includes(q) || d.scientificName.toLowerCase().includes(q)) &&
    (diet    === 'all' || d.diet    === diet) &&
    (period  === 'all' || d.period  === period) &&
    (habitat === 'all' || d.habitat === habitat)
  );
});
